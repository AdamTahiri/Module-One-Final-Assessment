const removeNumbersAtOddIndices = require("../problems/removeNumbersAtOddIndices")

describe("removeNumbersAtOddIndices", () => {
  test("Removes numbers at odd indices from a small set", () => {
    expect(removeNumbersAtOddIndices([1, 2, 3, 4, 5, 6, 7, 8, 9])).toEqual([
      1,
      3,
      5,
      7,
      9,
    ])
  })
  test("Removes numbers at odd indices with more values", () => {
    const input = [
      207,
      808,
      814,
      41,
      443,
      689,
      719,
      611,
      174,
      98,
      3,
      632,
      663,
      531,
      970,
      941,
      0,
      554,
      608,
      827,
      330,
      90,
      19,
      857,
      976,
      806,
      499,
      464,
      786,
      590,
      833,
      721,
      898,
      441,
      272,
      37,
      812,
      705,
      35,
      987,
      52,
      63,
      227,
      388,
      267,
      574,
      413,
      262,
      698,
      48,
    ]
    const expected = [
      207,
      814,
      443,
      719,
      174,
      3,
      663,
      970,
      0,
      608,
      330,
      19,
      976,
      499,
      786,
      833,
      898,
      272,
      812,
      35,
      52,
      227,
      267,
      413,
      698,
    ]
    expect(removeNumbersAtOddIndices(input)).toEqual(expected)
  })
})
